#include <iostream>

using std::cout;
using std::endl;

int main()
{
    int slices = 5; 
    /**
     * int is the datatype
     * slices is the variable name
     * = is the assigment operator
     * 5 is the value
    */

    cout << "There are " << slices << " slices of pizza" << endl; 
    /**
     * Simply separate by << for concantenation 
     * endl is a function that moves onto the next line
    */

    return 0;
}